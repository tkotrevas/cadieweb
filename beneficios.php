                <h2>MAS O QUE A VIAGEM ME PROPORCIONA?.</h2>

            </header>
            <div class="content">
                <p>1. Viajar é o melhor remédio de todos e torna você mais saudável
                <P>
                    Viajar deixa você mais jovem. Isso porque você se torna uma pessoa mais saudável ao viajar.
                    É isso que um estudo recente feito em parceria por algumas associações renomadas dos Estados Unidos
                    mostra.
                    Segundo o estudo, homens que não tiram férias anuais têm 30% a mais de chances de sofrerem ataques
                    cardíacos. Os benefícios da viagem são quase instantâneos, 89% dos viajantes que participaram do
                    estudo disseram que sentiram o nível de stress baixar já no segundo dia de viagem.
                    Os estudos levaram em consideração o histórico de doença das pessoas e renda. E mesmo assim os
                    benefícios para a saúde física foram percebidos em todos que viajavam.
                    Ótima notícia, não é mesmo?
                <p>2. Viajar aumenta sua felicidade e satisfação</p>
                A viagem começa muito antes de embarcar no aeroporto, rodoviária, ou mesmo sair com o carro da garagem.
                Tudo começa no planejamento. E um estudo da Cornell University mostra que já nessa etapa as pessoas se
                mostram mais felizes.
                Essa pesquisa mostrou que a antecipação por uma viagem traz mais benefícios para a mente do que a
                antecipação pela compra de algo físico. Ou seja, planejar uma viagem traz mais felicidade do que
                planejar a compra de uma nova TV.
                Isso porque quando viajamos, nós desfrutamos ativamente dessa conquista e criamos lembranças duradouras
                desses momentos, enquanto a satisfação de uma compra é momentânea e efêmera.<p></p>
                <p>3. Viajar diminui o risco de depressão</p>
                Bem, essa é uma vantagem que provavelmente você já imaginava existir, certo?
                Apesar de a depressão ainda ser um tabu em nossa sociedade, em grande parte pela falta de conhecimento
                das pessoas, é um problema crescente.
                Ao viajar, desenvolvemos lembranças positivas e essas lembranças precisam ser formadas durante a viagem,
                o que faz com que o corpo produza uma série de hormônios ligados à felicidade, como a Endorfina, por
                exemplo.
                Quando nos expomos ao sol, produzimos Serotonina, que é um forte combatente da depressão.
                Além disso, uma viagem proporciona produção de Dopamina, que se manifesta quando tomamos um primeiro
                passo rumo a uma novo objetivo e quando alcançamos essa meta. Por exemplo, quando reservamos um hotel e
                quando chegamos e fazemos check-in.
                Por fim, ao viajar com alguém especial, seja amigo ou companheiro, desenvolvemos mais intimidade e
                estimulamos a produção de oxitocina, que estreita os laços emocionais.
                Tudo isso é responsável pela queda de índices de depressão em pessoas que viajam de férias pelo menos
                uma vez por ano.<p></p>
                <p>4. Viajar aumenta a sua criatividade</p>
                Ficar preso a uma rotina limita a nossa visão de mundo e isso interfere diretamente a nossa
                criatividade.
                Ao viajar, nos colocamos em contato com muitas coisas novas: pessoas, culturas, culinária etc. Tudo isso
                aguça a mente e libera novas ideias. Você pode não trabalhar diretamente com criatividade, mas ela é
                importante para a vida cotidiana.
                Uma visão criativa ajuda a resolver problemas de forma mais simples e isso diminui drasticamente o
                stress.<p></p>
                <p>5. Viajar melhora sua resiliência
                <p>
                    A vida é feita de mudanças e escolhas. Alguns não estão preparados para elas.
                    Ao viajar e entrar em contato com novas culturas e estilos de vida, a mente se abre para novas
                    possibilidades e visões de mundo. Além disso, imprevistos são comuns em uma viagem e o tempo
                    restrito nos obriga a lidar com eles diretamente.
                    Ou seja, a resiliência, que é a capacidade de se adaptar à mudança ou situações pouco agradável, se
                    fortalece muito.
            </div>

