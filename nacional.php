							<p>Em setembro, a intenção de viagem do brasileiro para os próximos meses atingiu o maior percentual do ano, 24,3%. O dado do estudo Sondagem do Consumidor – Intenção de Viagem, do Ministério do Turismo, representa um crescimento de 8% em relação ao mês de setembro do ano anterior. Para 80,3% dos futuros viajantes, a ideia é desbravar os destinos nacionais e desfrutar de belos cenários naturais, rica gastronomia e manifestações culturais.

							<p>O estudo também revela que a Região Nordeste segue como a mais desejada por estes turistas, com 48% das preferências. Este índice representa um crescimento de 23% no desejo de visitar os destinos do Nordeste. A Região Sul ficou em segundo lugar com 25,3% das intenções de visita, seguida pelo Sudeste (19,2%), Centro-Oeste (4%) e Norte (3,5%).

							<p>“Hoje, cerca de 60 milhões de brasileiros viajam pelo País, mas há ainda 70 milhões prontos para entrar nesse mercado. O fortalecimento do turismo doméstico, em suas múltiplos formas - negócios, ecológico, esportivo, cultural, religioso - é um caminho que precisa ser explorado”, afirmou o ministro do Turismo, Marx Beltrão.

							<p>Em relação aos meios de hospedagem, 46,3% dos brasileiros que manifestaram intenção de viajar desejam hospedar-se em hotéis e pousadas durante a viagem dos próximos seis meses. A segunda opção mais desejada pelos viajantes foi a casa de parentes e amigos (42,5%), seguida pelos meios de alternativos de hospedagem que somaram 11,2% das preferências.

							<p>Outro destaque do levantamento do Ministério do Turismo revela que a intenção de viajar de avião nos próximos meses cresceu 9,6% em relação ao mesmo período do ano anterior, registrando o índice de 56,8% das preferências dos potenciais turistas. O automóvel foi o segundo meio de transporte preferido para a próxima viagem com o índice de 30,4%, seguido pelo ônibus com 11,3%.

							<p>ESTUDO – A Sondagem do Consumidor – Intenção de Viagem é realizada todos os meses em Belo Horizonte, Brasília, Porto Alegre, Recife, Rio de Janeiro, Salvador e São Paulo. Juntas, as sete capitais brasileiras monitoradas representam 70% do fluxo turístico do Brasil.
